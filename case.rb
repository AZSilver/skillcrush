puts "whats your favorite color?"
fav_color = gets.chomp

case fav_color
when 'red'
puts "Red like fire!"
when 'orange'
puts "Orange like an orange."
when 'yellow'
puts "Like the yellow sun!"
when 'green'
puts "Like the green earth!"
when 'blue'
puts "Blue like the water!"
when 'silver'
puts "Just like Silverman!"
else 
puts "Hmm, well I don't know what color that color is!"
end
